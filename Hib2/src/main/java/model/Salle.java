package model;


public class Salle {
	
	private int id;
	private String libelle;
	
	
	public int getId() {
		return id;
	}
	
	public Salle()
	{
		
	}
	
	
	public void setId(int id) {
		this.id = id;
	}
	public String getLibelle() {
		return libelle;
	}
	public void setLibelle(String libelle) {
		this.libelle = libelle;
	}
	
	

}
